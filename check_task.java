    private void upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == up) {
            int emp_id = Integer.parseInt(id.getText());
            try {
                Connection c = database.task_db.connect();
                Statement sc = c.createStatement();
                String sql = "update task set task_id='" + Integer.parseInt(tid.getText()) + "' , task_name='" + tname.getText() + "' , check_col='" + Integer.parseInt(check.getText()) + "'  where s_emp_id = '" + emp_id + "' ";
                int rs = sc.executeUpdate(sql);
                if (rs == 1) {
                    JOptionPane.showMessageDialog(null, "Operation has done succesfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    new emp_view_task().show_view_tasks();
                    this.dispose();
                }

            } catch (SQLException ee) {
                System.out.println(ee.getMessage());
            }
        }
    }//GEN-LAST:event_upActionPerformed

