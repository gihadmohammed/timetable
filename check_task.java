    public check_task(int value) {
        initComponents();
        id.setText(value + "");
        try {
            Connection c = database.task_db.connect();
            Statement sc = c.createStatement();
            String sql = "select task_id ,task_name,check_col from task where s_emp_id='" + value + "'";
            ResultSet rs = sc.executeQuery(sql);
            while (rs.next()) {
                tid.setText(rs.getString("task_id"));
                tname.setText(rs.getString("task_name"));
                check.setText(rs.getString("check_col"));
            }
        } catch (SQLException ee) {
            System.out.println(ee.getMessage());
        }
    }

